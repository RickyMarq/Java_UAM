package javaapplication3;


public class Calculadora {
    
    public double operacaoSoma(double num1, double num2) {
        return (num1 + num2);
    }
    
    public double operacaoSub(int num1, double num2) {
        return (num1 - num2);
    }
    
    public double operacaoDiv(int num1, int num2) {
        return (num1 / num2);
    }
    
    public double operacao(int num1, short num2) {
        return 1;
    }
     
}
